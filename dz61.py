n = int(input("Введите количество задаваемых целых чисел: "))
#TODO проверка на дурака на ввод
spisok = []


z = 0
#тест
# n = 10
# spisok = [6, -1, 0, 5, -7, 0, 1, 2, 0, 4]
for i in range(n):
    spisok.append(int(input(f"введите {i+1} целое число: ")))


for k in spisok:
    if k != 0:
        z += 1
print(f"Количество введённых целых чисел, не равных нулю - {z} из {n} введённых")